    private void JButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonOKActionPerformed
        
        try{
            URL url = new URL("http://localhost:8084/p2sserver/LoginServlet?login="+this.JTextFieldLogin.getText()+"&password="+this.JTextFieldMDP.getText());
            
            BufferedReader in = new BufferedReader(
                    new InputStreamReader(
                    url.openStream()));
            
            //Récupération du fluxXml envoyé par la Servlet : LoginServlet contenant toutes les données de l'utilisateur
            String fluxXml = new String("");
            String inputLine;
            while ((inputLine = in.readLine()) != null)
                fluxXml += inputLine;
            
            ParserXMLLog parser = new ParserXMLLog(fluxXml);
            
            //Reader reader = new StringReader(inputLine);
            if(parser.lireFonction().compareTo("sup") == 0) {
                ((JFrameP2S)this.getParent()).utilisateur = new Superviseur(this.JTextFieldLogin.getText(),parser.lireProjets());
            } else if(parser.lireFonction().compareTo("dir") == 0) {
                ((JFrameP2S)this.getParent()).utilisateur = new Directeur(this.JTextFieldLogin.getText(),parser.lireMembres());
            }            
            in.close();
        } catch(MalformedURLException e1){
            e1.printStackTrace();
        } catch(IOException e2){
            e2.printStackTrace();
        }
        this.dispose(); 
    }//GEN-LAST:event_JButtonOKActionPerformed
    
